export default ({ scope, ano, cargo }) => {
  const list = data.split('\n');
  let suplementar = false;
  const anoArray = [ano, ano * 1 + 1, ano * 1 + 2];

  for (let i = 0; i < list.length; i += 1) {
    const line = list[i].trim();
    if (!line) {
      continue;
    }
    const split = line.split('|');
    const cidades = split[2].split(',');

    const cidadeTrimmed = scope
      .replace(/[\s ]*/gim, '')
      .trim()
      .toLocaleLowerCase();

    cidades.forEach((nome) => {
      const nomeTrimmed = nome
        .replace(/[\s ]*/gim, '')
        .trim()
        .toLocaleLowerCase();

      if (nomeTrimmed === cidadeTrimmed) {
        suplementar = true;
      }
    });

    if (suplementar) {
      const anoReg = new RegExp(anoArray.join('|'), 'mig');
      if (!split[0].match(anoReg)) {
        suplementar = false;
      }
    }

    if (suplementar) {
      if (split?.[3] && split[3] !== cargo) {
        suplementar = false;
      }
      if (!split?.[3] && cargo !== 'prefeito') {
        suplementar = false;
      }
    }

    if (suplementar) {
      break;
    }
  }

  return suplementar;
};
