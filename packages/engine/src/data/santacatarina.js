const getAno = (data) => {
  const result = {};
  const dataSplit = data.split("\n").map((item) => item.trim());

  dataSplit.forEach((line) => {
    const [name, comparecimento, validos, brancos, nulos] = line.split(" | ");

    const votos = validos * 1 + brancos * 1 + nulos * 1;
    const cod = codigo[name];

    if (!cod) {
      return;
    }

    if (!result[cod]) {
      result[cod] = {
        prefeito: {
          brancos: 0,
          nulos: 0,
          validos: 0,
          nominais: 0,
          total: 0,
        },
      };
    }

    const current = result[cod].prefeito;
    current.total += votos;
    current.validos += validos * 1;
    current.nominais += validos * 1;
    current.brancos += brancos * 1;
    current.nulos += nulos * 1;
  });

  return result;
};

