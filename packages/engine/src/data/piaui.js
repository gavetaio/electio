const getAno = (data) => {
  const result = {};
  const dataSplit = data.split("\n").map((item) => item.trim());
  dataSplit.forEach((line) => {
    const [codigo, tipo, votavel, votosString] = line.split(" ");
    const votos = parseInt(votosString) || 0;

    if (!result[codigo]) {
      result[codigo] = {
        prefeito: {
          brancos: 0,
          nulos: 0,
          validos: 0,
          nominais: 0,
          total: 0,
        },
        vereador: {
          brancos: 0,
          nulos: 0,
          validos: 0,
          nominais: 0,
          legenda: 0,
          total: 0,
        },
      };
    }
    const current =
      tipo === "1" ? result[codigo].prefeito : result[codigo].vereador;

    current.total += votos;

    if (votavel === "10") {
      current.validos += votos;
      current.nominais += votos;
    }

    if (votavel === "99") {
      current.legenda += votos;
      current.validos += votos;
    }

    if (votavel === "95") {
      current.brancos += votos;
    }

    if (votavel === "96") {
      current.nulos += votos;
    }
  });
  return result;
};

